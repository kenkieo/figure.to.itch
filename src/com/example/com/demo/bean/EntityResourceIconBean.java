package com.example.com.demo.bean;

public class EntityResourceIconBean {

	public String id;
	public String name;
	public String url;
	public String big_url;
	public String category_id;
	public String pure_color;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getBig_url() {
		return big_url;
	}

	public void setBig_url(String big_url) {
		this.big_url = big_url;
	}

	public String getCategory_id() {
		return category_id;
	}

	public void setCategory_id(String category_id) {
		this.category_id = category_id;
	}

	public String getPure_color() {
		return pure_color;
	}

	public void setPure_color(String pure_color) {
		this.pure_color = pure_color;
	}

}
